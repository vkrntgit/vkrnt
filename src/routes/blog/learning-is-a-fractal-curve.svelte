<h2>Learning is a fractal curve</h2>
<br><br>
How you treat a small lesson in any technology decides how you evolve in that area over the years. 
<br><br>
Everyone in tech starts at almost the same line. Yet some race ahead and turn into SMEs. We all have the same 24 hours in the day. Some make more out of it than others. I think the differentiator is how we exploit each hour. Learning new tech - fast - gives you a huge advantage. Many prefer the approach of courses and certification. There is nothing wrong with leaning on courses. Certifications are a fantastic way to show the world what you know. Beyond these objectives, when it comes to <i>really</i> getting a handle of something, hands-on practice is the unbeatable champion. 
<br><br>
Learning this way takes longer though. Learning a completely new platform/tool/tech can be daunting. Coffee can only carry you for a couple of hours. An easy way to learn consistently and grow in a new area is to break down the subject matter into bite size lessons. I have experienced a significant qualititative difference in my understanding and my ability to apply knowledge when I focus all my attention on a ten minute information nugget - as against racing through a hundred pages or watching a web course at 2x speed. The best measure of success is how easily you move from learning to application. 
<br><br>
The jump from learning to applying knowledge is tougher when you absorb a ten hour course or complete a three hundred page book over a weekend. Instead if you pace the learning, try your hand at what you picked and keep firming the body of knowledge on a topic, it becomes so much more easier to start using the new tech. <br><br>

 
